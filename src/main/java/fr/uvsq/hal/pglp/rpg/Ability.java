/**
 * La classe <code>Ability</code> représente une caractéristique d'un personnage.
 *
 * @author hal
 * @version 2022
 */
public enum Ability {
  /** Force. */
  Strength,
  /** Dextérité. */
  Dexterity,
  /** Constitution. */
  Constitution,
  /** Intelligence. */
  Intelligence,
  /** Sagesse. */
  Wisdom,
  /** Charisme. */
  Charisma;
}
